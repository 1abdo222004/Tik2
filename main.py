def fmt_adhkar(item, category="misc", emoji=True, style="fancy"): 
    if not item: 
        return "❌ تعذّر جلب الذكر، حاول مجدداً." 
 
    icon  = _CAT_ICON.get(category, "📿") 
    title = _CAT_AR.get(category, "أذكار") 

    # ── استخراج النص والعدد والفضل ──
    texts  = []
    counts = []
    fadls  = []

    # ✅ دعم تنسيق الأذكار المخصصة (morning / sleep)
    if isinstance(item, dict) and "content" in item:
        txt = item.get("content", "").strip()
        cnt = item.get("count", 1)
        fadl = item.get("fadl", "")
        if txt:
            texts.append(txt)
            counts.append(cnt)
            fadls.append(fadl)

    # دعم تنسيق API الخارجي (adhkar_evening / misc)
    elif isinstance(item, dict) and "array" in item:
        api_title = item.get("category") or item.get("title") or ""
        arr = item["array"]
        if isinstance(arr, list):
            for x in arr:
                if isinstance(x, dict):
                    txt = x.get("text", "").strip()
                    if txt:
                        texts.append(txt)
                        counts.append(x.get("count", 1))
                        fadls.append("")
        elif isinstance(arr, dict):
            txt = arr.get("text", "").strip()
            if txt:
                texts.append(txt)
                counts.append(arr.get("count", 1))
                fadls.append("")

    else:
        txt = (item.get("content") or item.get("text") or str(item)).strip()
        if txt:
            texts.append(txt)
            counts.append(item.get("count", 1))
            fadls.append(item.get("fadl", ""))

    if not texts:
        texts = [str(item)]
        counts = [1]
        fadls  = [""]

    # ── بناء الرسالة ── 
    msg = ""
    if style == "fancy":
        msg += f"{icon} *{title}*\n"
        msg += "━━━━━━━━━━━━━━━\n\n"

    for i, t in enumerate(texts):
        t = t.replace("((", "").replace("))", "").strip()
        msg += f"🕋 {t}\n"

        cnt = counts[i] if i < len(counts) else 1
        try:
            cnt_int = int(cnt)
        except (ValueError, TypeError):
            cnt_int = 1

        if cnt_int > 1:
            msg += f"🔄 {cnt_int} مرات\n"

        fadl = fadls[i] if i < len(fadls) else ""
        if fadl:
            msg += f"✨ *الفضل:* {fadl}\n"

        msg += "\n"

    return msg.strip()


