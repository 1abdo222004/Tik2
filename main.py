def _edit_or_send(cid, mid, text, markup): 
    try: 
        bot.edit_message_text(text, cid, mid, 
                              parse_mode="Markdown", reply_markup=markup) 
    except Exception: 
        bot.send_message(cid, text, parse_mode="Markdown", reply_markup=markup) 
 
 
