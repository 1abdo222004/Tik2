def _parse_custom_hadiths(raw: str) -> list:
    """تحويل الأحاديث النصية إلى قائمة من القواميس."""
    hadiths = []
    for line in raw.strip().splitlines():
        line = line.strip()
        if not line:
            continue
        # استخراج الرقم من البداية
        m = re.match(r'^(\d+)-\s*(.+)$', line)
        if m:
            num  = int(m.group(1))
            text = m.group(2).strip()
            hadiths.append({
                "hadithnumber": num,
                "hadith": text,
                "chapter": "",
            })
    return hadiths


