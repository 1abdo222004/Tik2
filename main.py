def clean_hadith_text(text: str) -> str: 
    if not text: 
        return text 
    end_markers = [ 
        'قَالَ أَبُو عَبْد', 'قال أبو عبد', 
        'وَأَخْبَرَنِي', 'وأخبرني', 
        'وَأَخْبَرَنَا', 'وأخبرنا', 
    ] 
    for marker in end_markers: 
        idx = text.find(marker) 
        if idx > 50: 
            text = text[:idx].strip().rstrip('‏.') 
    return text 
 
 
